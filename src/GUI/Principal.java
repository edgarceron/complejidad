    private void botonCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCargarActionPerformed
        JFileChooser chooser=new JFileChooser();
        chooser.showOpenDialog(this);
        File archivo=chooser.getSelectedFile();
        int linea = 1;
        int numParcelas = 0;
        int[] duracionCosecha = new int[0];
        int sumaDuracion = 0;
        int[][] utilidadEsperada = new int[0][0]; 
        String aux;
        boolean error = false;
        try{
            if(archivo!=null)
            {     
                FileReader archivos= new FileReader(archivo);
                BufferedReader br=new BufferedReader(archivos);
                while((aux=br.readLine())!=null)
                {
                    areaInformacion.append(aux.trim() + "\n");
                    if(linea == 1){
                        numParcelas = Integer.parseInt(aux.trim());
                        duracionCosecha = new int[numParcelas];
                        linea++;
                    }
                    else if(linea == 2){
                        StringTokenizer st = new StringTokenizer(aux.trim()," ");
                        for(int i = 0; i < numParcelas; i++){
                            duracionCosecha[i] = Integer.parseInt(st.nextToken());
                        }
                        linea++;
                    }
                    else if(linea == 3){
                        sumaDuracion = Integer.parseInt(aux.trim());
                        utilidadEsperada = new int[numParcelas][sumaDuracion];
                        linea++;
                    }
                    else{
                        StringTokenizer st = new StringTokenizer(aux.trim()," ");
                        for(int i = 0; i < sumaDuracion; i++){
                            utilidadEsperada[linea-4][i] = Integer.parseInt(st.nextToken());
                        }
                        linea++;
                    }
                }
                br.close();
            }    
        }
        catch(IOException ex){
            error = true;
            JOptionPane.showMessageDialog(this,
           "\nNo se ha encontrado el archivo",
                 "ADVERTENCIA!!!",JOptionPane.WARNING_MESSAGE);
        }
        catch(Exception e){
            error = true;
            JOptionPane.showMessageDialog(this,
           "\nOcurrio un error inesperado, verifique el archivo",
                 "ADVERTENCIA!!!",JOptionPane.WARNING_MESSAGE);
            e.printStackTrace();
        }
        finally{
            if(!error){
                if(archivo!=null) labelArchivo.setText(archivo.getPath());
                i = new InfoParcelas(numParcelas, duracionCosecha, sumaDuracion, utilidadEsperada);
            }  
            else{
                
            }
        }
    }//GEN-LAST:event_botonCargarActionPerformed
 
